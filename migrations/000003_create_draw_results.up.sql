CREATE TABLE draw_results
(
--    id              serial4   NOT NULL,
    id              int4 GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    draw_id         int4                                  NOT NULL,
    win_combination _int4                                 NOT NULL,
    result_time     timestamp DEFAULT CURRENT_TIMESTAMP   NOT NULL,
    CONSTRAINT draw_results_pkey PRIMARY KEY (id)
);

CREATE INDEX idx_draw_results_draw_id ON draw_results (draw_id);
